// My age as a number
const myAge = 26;

// The first two years
let earlyYears = 2;
earlyYears *= 10.5;

// Account for the first two years
laterYears = myAge - 2;
// Calculate the number of dog years accounted for by my later years
laterYears *= 4;

// My age in dog years
myAgeInDogYears = earlyYears + laterYears;
// Write my name in string
myName = "Beno".toLowerCase();

// Display name and age in dog years
console.log(
  `My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`
);
